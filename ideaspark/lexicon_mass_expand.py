def _grid(a: list[str], b: list[str], cap: int) -> list[str]:
    out: list[str] = []
    for x in a:
        for y in b:
            out.append(f"{x}{y}")
            if len(out) >= cap:
                return out
    return out


def _grid3(a: list[str], b: list[str], c: list[str], cap: int) -> list[str]:
    out: list[str] = []
    for x in a:
        for y in b:
            for z in c:
                out.append(f"{x}{y}{z}")
                if len(out) >= cap:
                    return out
    return out


def _take(lst: list[str], cap: int) -> list[str]:
    return lst[:cap] if len(lst) > cap else lst


